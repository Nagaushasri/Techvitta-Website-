export const getBlogBySlug = (slug) => {
  return blogData.find(blog => blog.slug === slug);
};

export const getRecentBlogs = (excludeId = null, limit = 5) => {
  return blogData
    .filter(blog => blog.id !== excludeId)
    .sort((a, b) => new Date(b.date) - new Date(a.date))
    .slice(0, limit);
};

export const getPopularBlogs = (excludeId = null, limit = 5) => {
  // For demo purposes, we'll use a simple popularity score based on ID
  // In a real app, this would be based on views, likes, shares, etc.
  return blogData
    .filter(blog => blog.id !== excludeId)
    .sort((a, b) => b.id - a.id)
    .slice(0, limit);
};
